package com.sdyx.hall.service.internal.customer.echannel.model;

public class RenewalInfo {

	private String caNo;
	
	private String pOCo;
	
	private String pPCo;
	
	private String mPCo;
	
	private Long mouth;

	public String getCaNo() {
		return caNo;
	}

	public void setCaNo(String caNo) {
		this.caNo = caNo;
	}

	public String getpOCo() {
		return pOCo;
	}

	public void setpOCo(String pOCo) {
		this.pOCo = pOCo;
	}

	public String getpPCo() {
		return pPCo;
	}

	public void setpPCo(String pPCo) {
		this.pPCo = pPCo;
	}

	public String getmPCo() {
		return mPCo;
	}

	public void setmPCo(String mPCo) {
		this.mPCo = mPCo;
	}

	public Long getMouth() {
		return mouth;
	}

	public void setMouth(Long mouth) {
		this.mouth = mouth;
	}

}
